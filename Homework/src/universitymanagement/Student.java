package universitymanagement;

public class Student {
	String studentid;
	String name;
	int attendanceper;
	public Student() {
		super();
	}
	public Student(String studentid, String name, int attendanceper) {
		super();
		this.studentid = studentid;
		this.name = name;
		this.attendanceper = attendanceper;
	}
	
	

}
